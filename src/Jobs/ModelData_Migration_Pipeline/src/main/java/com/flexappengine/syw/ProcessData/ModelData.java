package com.flexappengine.syw.ProcessData;

public class ModelData {
    private String memberId;
    private Integer offerValue;
    private String campaignName;
    private String store;
    private Integer divNo;
    private String divName;

    public String getMemberId() {
	return memberId;
    }

    public void setMemberId(String memberId) {
	this.memberId = memberId;
    }

    public Integer getOfferValue() {
	return offerValue;
    }

    public void setOfferValue(Integer offerValue) {
	this.offerValue = offerValue;
    }

    public String getCampaignName() {
	return campaignName;
    }

    public void setCampaignName(String campaignName) {
	this.campaignName = campaignName;
    }

    public String getStore() {
	return store;
    }

    public void setStore(String store) {
	this.store = store;
    }

    public Integer getDivNo() {
	return divNo;
    }

    public void setDivNo(Integer divNo) {
	this.divNo = divNo;
    }

    public String getDivName() {
	return divName;
    }

    public void setDivName(String divName) {
	this.divName = divName;
    }

}